package ba.corder.kr.day4;

public class NestForTest {
			public static void main(String[] args) {
				
				
				
				System.out.println("5에서1까지의수입니다.");
				for(int i=5;i>=1; i--) {
					for(int j=1; j<=i;j++) {
						System.out.print("*"+"  ");
				}
				System.out.println();
			}
			}
}
